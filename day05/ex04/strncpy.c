#include <stdio.h>
#include <string.h>

char *ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int main ()
{
	char src[]="tutorial";
	char dest[4];

	ft_strncpy(dest, src, 1);

	printf("Final copied string : %s\n", dest);
    //resultat: Final copied string : t
	return(0);
}
