#include <stdio.h>

int     ft_sqrt(int nb)
{
    int i;

    i = 1;
    if ( nb < 0)
        return(0);
    while (i * i < nb)
        i++;
    if (i * i  == nb)
        return(i);
    else
        return(0);
}

int main()
{
    printf("%d", ft_sqrt(48));
    return 0;
}
