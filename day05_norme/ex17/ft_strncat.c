char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i] && i < nb)
	{
		if (dest[i + 1] == '\0' && i < nb)
		{
			while (src[j] && i < nb)
			{
				dest[i + 1] = src[j];
				if (src[j + 1] == '\0')
					dest[i + 2] = '\0';
				j++;
				i++;
			}
		}
		i++;
	}
	return (dest);
}
