#include <stdio.h>

void ft_swap(int *a, int *b)
{
    int swap;
    
    swap = *a;
    *a = *b;
    *b = swap;
}

int main ()
{
    int a = 3;
    int b = 5;
    
    ft_swap(&a,&b);
    printf("%d", a);
    printf("\n");
    printf("%d\n", b);
    
}
