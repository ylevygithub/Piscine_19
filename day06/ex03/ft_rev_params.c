void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int		i;
	int		j;

	j = argc - 1;
	while (j >= 1)
	{
		i = 0;
		while (argv[j][i] != '\0')
		{
			ft_putchar(argv[j][i]);
			i++;
		}
		j--;
		ft_putchar('\n');
	}
	return (0);
}
